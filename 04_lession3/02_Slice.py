name = "Alice"
print("Start with 0 to 3 but 3 is not included  :",name[0:3])
print("Start with 0 to 3 but 3 is not included  :",name[-4:-1]) # negative value start from front-to-End(length of value   to decrease)
print(name[1:]) # It is same as print(name[1:5])
print(name[:-1]) # It is same as print(name[-5:-1])